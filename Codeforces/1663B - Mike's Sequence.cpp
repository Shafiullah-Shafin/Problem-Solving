    if(r < 1200) {
        cout << 1200 << '\n';
    }
    else if(r < 1400) {
        cout << 1400 << '\n';
    }
    else if(r < 1600) {
        cout << 1600 << '\n';
    }
    else if(r < 1900) {
        cout << 1900 << '\n';
    }
    else if(r < 2100) {
        cout << 2100 << '\n';
    }
    else if(r < 2300) {
        cout << 2300 << '\n';
    }
    else if(r < 2400) {
        cout << 2400 << '\n';
    }
    else if(r < 2600) {
        cout << 2600 << '\n';
    }
    else {
        cout << 3000 << '\n';
    }
